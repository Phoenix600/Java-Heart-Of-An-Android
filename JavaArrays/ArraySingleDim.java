public class ArraySingleDim {


    public static void main(String[] args) {
        int A[] = {12,12,12,12,12,12};
        int B[][] = {{1,2,3,4},{1,2,3,4}};

        System.out.println(A);  // [ => 1D Array and I for Integer type
        System.out.println(B); // [[ ==> 2D array and I for Integer type
    }
    
}
